/**
 * Import NTC cost indices (INCTL + INCTF) — generates SQL for db push.
 * Usage: node scripts/import-ntc-indices.cjs > data/ntc_indices_import.sql
 */

const fs = require('fs');
const path = require('path');

const DATA_DIR = path.join(__dirname, '..', 'data');

function parseCSV(filePath) {
  const content = fs.readFileSync(filePath, 'utf-8');
  const lines = content.trim().split('\n');
  const headers = lines[0].split(',');
  const rows = [];
  for (let i = 1; i < lines.length; i++) {
    const values = lines[i].split(',');
    const row = {};
    for (let j = 0; j < headers.length; j++) {
      const key = headers[j].trim();
      const val = (values[j] || '').trim();
      if (val === '') {
        row[key] = null;
      } else if (!isNaN(Number(val)) && key !== 'period' && key !== 'index_type') {
        row[key] = Number(val);
      } else {
        row[key] = val;
      }
    }
    rows.push(row);
  }
  return rows;
}

function sqlVal(v) {
  if (v === null || v === undefined) return 'NULL';
  if (typeof v === 'number') return v.toString();
  return `'${v}'`;
}

function main() {
  const sqlLines = [];
  sqlLines.push('-- NTC Cost Indices Import — últimos 24 meses');
  sqlLines.push('-- Generated by scripts/import-ntc-indices.cjs');
  sqlLines.push('');

  // INCTL
  const inctlFile = path.join(DATA_DIR, 'ntc_inctl_series.csv');
  if (fs.existsSync(inctlFile)) {
    const rows = parseCSV(inctlFile);
    const cutoff = '2024-02-01';
    const filtered = rows.filter(r => r.period >= cutoff);
    console.error(`INCTL: ${rows.length} total, ${filtered.length} from ${cutoff}`);

    if (filtered.length > 0) {
      sqlLines.push('-- INCTL indices');
      sqlLines.push('INSERT INTO public.ntc_cost_indices (index_type, period, distance_km, pickup_km, index_value) VALUES');
      const vals = filtered.map(r =>
        `  (${sqlVal(r.index_type)}, ${sqlVal(r.period)}, ${sqlVal(r.distance_km)}, ${sqlVal(r.pickup_km)}, ${sqlVal(r.index_value)})`
      );
      sqlLines.push(vals.join(',\n') + '\nON CONFLICT (index_type, period, COALESCE(distance_km, 0), COALESCE(pickup_km, 0)) DO UPDATE SET index_value = EXCLUDED.index_value;');
      sqlLines.push('');
    }
  }

  // INCTF
  const inctfFile = path.join(DATA_DIR, 'ntc_inctf_series.csv');
  if (fs.existsSync(inctfFile)) {
    const rows = parseCSV(inctfFile);
    const cutoff = '2024-02-01';
    const filtered = rows.filter(r => r.period >= cutoff);
    console.error(`INCTF: ${rows.length} total, ${filtered.length} from ${cutoff}`);

    if (filtered.length > 0) {
      sqlLines.push('-- INCTF indices');
      sqlLines.push('INSERT INTO public.ntc_cost_indices (index_type, period, distance_km, pickup_km, index_value) VALUES');
      const vals = filtered.map(r =>
        `  (${sqlVal(r.index_type)}, ${sqlVal(r.period)}, ${sqlVal(r.distance_km)}, ${sqlVal(r.pickup_km)}, ${sqlVal(r.index_value)})`
      );
      sqlLines.push(vals.join(',\n') + '\nON CONFLICT (index_type, period, COALESCE(distance_km, 0), COALESCE(pickup_km, 0)) DO UPDATE SET index_value = EXCLUDED.index_value;');
    }
  }

  // Output SQL to stdout
  process.stdout.write(sqlLines.join('\n') + '\n');
}

main();
